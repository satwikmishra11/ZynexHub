import { createClient } from '@supabase/supabase-js'

const supabaseUrl = import.meta.env.VITE_SUPABASE_URL
const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY

// Validate environment variables
if (!supabaseUrl || supabaseUrl === 'your_supabase_project_url_here') {
  throw new Error(
    'Missing VITE_SUPABASE_URL environment variable. Please set up your Supabase project by clicking "Connect to Supabase" in the top right corner.'
  )
}

if (!supabaseAnonKey || supabaseAnonKey === 'your_supabase_anon_key_here') {
  throw new Error(
    'Missing VITE_SUPABASE_ANON_KEY environment variable. Please set up your Supabase project by clicking "Connect to Supabase" in the top right corner.'
  )
}

export const supabase = createClient(supabaseUrl, supabaseAnonKey)

